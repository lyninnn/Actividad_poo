public class Profile {
        private String color;
        private Integer size;
        public  Profile(){
                this.color="black";
                this.size=12;
        }


        public String getColor() {
                return color;
        }

        public void setColor(String color) {
                this.color = color;
        }

        public Integer getSize() {
                return size;
        }

        public void setSize(Integer size) {
                this.size = size;
        }
}
